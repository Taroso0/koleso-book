/** Мемоизация загрузчиков контента на время процесса.
 *
 *  Зачем. Контент неизменен внутри одной сборки, но загрузчики читали диск заново
 *  на каждый вызов: `getStory` — это `getAllStories().find(…)`, а `getAllStories` —
 *  это `readdirSync` + 34 × (`readFileSync` + gray-matter + zod). За одну прод-сборку
 *  набегало ~144 полных скана корпуса (по 3 на страницу рассказа + OG-карточки +
 *  sitemap + `BookSwitcher` внутри каждого рассказа) — около 4 900 чтений файлов и
 *  столько же zod-парсов там, где хватает одного прохода.
 *
 *  Почему только в проде. В dev-режиме кэш выключен намеренно: иначе правка
 *  `content/**` не подхватится без перезапуска сервера. В тестах (NODE_ENV=test)
 *  тоже выключен — моки `node:fs` рассчитывают на реальное чтение при каждом вызове.
 */
export function memoInBuild<T>(load: () => T): () => T {
  if (process.env.NODE_ENV !== "production") return load;
  let cached: { value: T } | null = null;
  return () => (cached ??= { value: load() }).value;
}
